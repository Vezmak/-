"""*Напишите процедуру, которая выводит на экран запись переданного ей числа в римской
системе счисления."""

num = int(input('Введите число: '))
r_nums = ""
S1 = {1: 'IVX', 10: 'XLC', 100: 'CDM', 1000: 'M  '}

for i in (1000, 100, 10, 1):
    if num // i != 0:
        a, b, c = S1[i]
        S = (a, a * 2, a * 3, a + b, b, b + a, b + 2 * a, b + 3 * a, a + c)
        r_nums += S[num // i - 1]
        num = num - (num // i) * i
print("Ваше число:", r_nums)